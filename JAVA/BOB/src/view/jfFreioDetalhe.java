    private void preencherLabels(){
        Auxiliar aux = new Auxiliar();
        lblDataDiffUlt.setText(""+aux.diffDataAtualString(freioManutencao.getDataMilis()));
        lblDataUltima.setText(""+aux.getDataString(freioManutencao.getDataMilis()));
        lblIdFreio.setText(""+idRoda);
        lblKmDiffUlt.setText(""+new VeiculoKMControl().getDiffUltimoKmByID(veiculo.getId(), freioManutencao.getKm()));
        lblKmUltima.setText(""+freioManutencao.getKm());
        lblValor.setText("");
    }
    private void limparCampos(){
        lblDataDiffUlt.setText("");
        lblDataUltima.setText("");
        lblIdFreio.setText("");
        lblKmDiffUlt.setText("");
        lblKmUltima.setText("");
        lblValor.setText("");
        preencherListView();
    }

    private void preencherListView(){
        DefaultListModel listModel = new DefaultListModel();
        int j=0;
        arraylist = freioManutencao.getArraylist();
        float valor=0;
        for(int i=arraylist.size()-1; i>=0; i--){
            String linha = "Nº " + i;
            linha = linha + ", Marca: " + arraylist.get(i).getMarca();
            linha = linha +", Modelo: " + arraylist.get(i).getModelo();
            linha = linha +", Valor: " + arraylist.get(i).getValor();
            linha = linha +", Info: " + arraylist.get(i).getInfo();
            listModel.add(j,linha);
            j++;
            valor+=arraylist.get(i).getValor();
        }
        jList.setModel(listModel);
        
        lblValor.setText(new Auxiliar().StringFloatReais(valor));
    }
    
    
    
    

