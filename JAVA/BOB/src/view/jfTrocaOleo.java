    private boolean validarKm(String km){
        try{
            int kmTroca = Integer.parseInt(km);
            if(kmTroca<=kmAtual){
                return  true;
            }else{
                aux.showMessageInformacao("Km maior que o último Km registrado. Atualize Km do Veiculo para continuar!", "Validação de valor");
            }
            
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpTrocaOleo.validarKM");
        }
        return false;
    }
    
    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        try{
            if(arraylistPedidoItem.size()>0){
                if(preencherVeiculoPreventivaFutura()){
                   Pedido pedido = new Pedido();
                   pedido.setId_veiculo(veiculo.getId());
                   pedido.setDatamilis(aux.getDataMilisAtual());
                   pedido.setKm(Integer.parseInt(txfKm.getText()));
                   pedido.setArraylist(arraylistPedidoItem);
                   pedido.setTipo(Pedido.OLEOFILTRO);
                   pedido.setInfo(txfInfo.getText());

                   PedidoControl pc = new PedidoControl();
                   if(pc.Inserir(pedido)){
                       pedido.setId(pc.getUltimoPedidoByIdVeiculo(veiculo.getId()).getId());
                       vpc.atualizarPreventivaByPedido(pedido, arrayListVeiculoPreventiva);
                       PedidoGeradoComSucesso();
                   }
                }
            }else{
                aux.showMessageInformacao("Acrescente pelo menos um item ao pedido", "Lista vazia");
            }
        }catch(Exception e){      }
    }//GEN-LAST:event_btnFinalizarActionPerformed
    private boolean preencherVeiculoPreventivaFutura(){//checar se os valores da tabela (km e data) está corretos e preencer o arraylist.
        VeiculoPreventiva vp;
        arrayListVeiculoPreventiva.clear();
        try{
            for(int i=0; i<jTable.getRowCount();i++){

                String km = jTable.getModel().getValueAt(i, 7).toString();
                //String data = jTable.getModel().getValueAt(i, 7).toString();
                vp = new VeiculoPreventiva();
                //vp.setData(aux.dataStringLong(data));
                vp.setId_veiculo(veiculo.getId());
                vp.setTipo(ic.getItemByID(arraylistPedidoItem.get(i).getIdItem()).getTipo());
                vp.setKmProx(Integer.parseInt(km));
                vp.setKm(kmAtual);
                arrayListVeiculoPreventiva.add(vp);
            }
        }catch(Exception e){
            aux.showMessageWarning(e.getMessage(), "Metodo PreencherVeiculoPreventiva");
            return false;
        }
        return true;
    }
