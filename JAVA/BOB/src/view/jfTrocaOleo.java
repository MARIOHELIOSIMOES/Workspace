    private void atualizarTabelaItens(){
        DefaultTableModel tableModel = new DefaultTableModelNaoEditavel();
        tableModel.addColumn("Nº");
        tableModel.addColumn("Tipo");
        tableModel.addColumn("Marca");
        tableModel.addColumn("Modelo");
        tableModel.addColumn("Valor (Unitário)");
        tableModel.addColumn("Quantidade");
        tableModel.addColumn("Valor Total");
        tableModel.addColumn("KM Próxima Troca");
       
        try{
            Auxiliar aux = new Auxiliar();
            Object[] linha = new Object[8];
            Item item = new Item();
            ItemControl ic = new ItemControl();
            int km = Integer.parseInt(txfKm.getText());
            float pedidoTotal = 0;
            for(int i = 0; i<arraylistPedidoItem.size(); i++){
                linha[0]= i+1;
                item = ic.getItemByID(arraylistPedidoItem.get(i).getIdItem());
                linha[1]= Item.TIPO_STRING[item.getTipo()];
                linha[2]=item.getMarca();
                linha[3]=item.getModelo();
                linha[4]=aux.StringFloatReais(arraylistPedidoItem.get(i).getValor());
                linha[5]=arraylistPedidoItem.get(i).getQuantidade();
                linha[6]=arraylistPedidoItem.get(i).getValorTotal();
                linha[7]= (km + arraylistKMProximo.get(i));
                tableModel.addRow(linha);
                pedidoTotal += arraylistPedidoItem.get(i).getValorTotal();
            }
            lblPedidoTotal.setText(aux.StringFloatReais(pedidoTotal));
        }catch (Exception e){
            aux.showMessageWarning("Verificar metodo preenchertableProxima jpTrocaOleo", "Inesperado");
        }finally{
            jTable.setModel(tableModel);
            jTable.getColumnModel().getColumn(0).setMaxWidth(30);
        }
    }
    
    
