    public void atualizarTela(){
        try{
            atualizarInfoVeiculo(veiculo);
            atualizarTabelaItens();
            int tipoindex = cbbTipo.getSelectedIndex();
            int itemindex = cbbItem.getSelectedIndex();
            preencherCBBTipo();
            cbbTipo.setSelectedIndex(tipoindex);
            cbbItem.setSelectedIndex(itemindex);
        }catch(Exception e){
        }
    }
    private void atualizarInfoVeiculo(Veiculo v){
        lblAno.setText(v.getAno()+"");
        lblCarroceria.setText(v.getCarroceria());
        lblConfiguracao.setText(Veiculo.confLabels[v.getConfiguracao()]);
        lblInfo.setText(v.getInfo());
        lblMarca.setText(v.getMarca());
        lblModelo.setText(v.getModelo());
        lblPlaca.setText(v.getPlaca());
        lblTipo.setText(v.getTipo());
        kmAtual = vkc.getUltimoKmByIDVeiculo(v.getId());
        lblKM.setText(kmAtual+"");
        txfKm.setText(kmAtual+"");
    }
    private void preencherCBBTipo(){
        cbbTipo.removeAllItems();
        for(int i: Item.OLEOSFILTROS){
            cbbTipo.addItem(Item.TIPO_STRING[i]);
        }
       /* for(int i: Item.FILTROS){
            cbbTipo.addItem(Item.TIPO_STRING[i]);
        }*/
        for(int i: Item.OUTROS){
            cbbTipo.addItem(Item.TIPO_STRING[i]);
        }
        cbbTipo.setSelectedIndex(0);
    }
    private void preencherCBBItem(int tipo){
        cbbItem.removeAllItems();
        try{
            arraylistItens = ic.getArrayListItemByTipo(new int[]{tipo}); 
            
            for(Item item: arraylistItens){
                cbbItem.addItem(item.getMarcaModelo());
            }
        }catch(Exception e){
            
        }
    }
    private void preencherCBBPrazo(){
        cbbPrazo.removeAllItems();
        for (int i: prazos){
            cbbPrazo.addItem(i+"");
        }
    }

