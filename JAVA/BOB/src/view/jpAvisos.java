    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        if(!jfPrincipal.isUserOperaOrAdmin()){
            aux.showMessagemSemPermissao();
            return;
        }
        try{
             String titulo = "", mensagem = "";
             titulo = aux.InputText("Informe o TÃ­tulo para representar o Aviso");
             if(titulo.equalsIgnoreCase("")){
                 return;
             }
             mensagem = aux.InputText("Informe a mensagem do Aviso");
             if(mensagem.equalsIgnoreCase("")){
                 return;
             }
             Aviso aviso = new Aviso();
             aviso.setTitulo(titulo);
             aviso.setMensagem(mensagem);
             aviso.setDatamilis(aux.getDataMilisAtual());
             aviso.setAtivo(true);
             aviso.setIdveiculo(veiculo.getId());

             if(aux.ConfirmarValor(titulo, mensagem+"\nConfirma a inclusÃ£o do aviso?")){
                 ac.inserir(aviso);
                 atualizarTela();
             }
         }catch(Exception e){
             aux.RegistrarLog(e.getMessage(), "jpAvisos.btnAdicionarActionPerfomed");
         }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirInativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirInativarActionPerformed
        if(!jfPrincipal.isUserOperaOrAdmin()){
            aux.showMessagemSemPermissao();
            return;
        }
        try{
            if(jTable.getSelectedRow()>=0 && arrayList.size()>=0){
             int index = arrayList.size() - 1 - jTable.getSelectedRow();
            if(ac.excluirInativar(arrayList.get(index))){ // passa o aviso
                if(arrayList.get(index).isAtivo()){ //se ele jÃ¡ era inativo exclui e atualiza tabela
                    lblData.setText("Inativo");
                    arrayList.get(index).setAtivo(false);
                }else{
                    preencherTabela();
                }
            }
        }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpAvisos.btnExcluirInativarActionPermormed");
        }
    }//GEN-LAST:event_btnExcluirInativarActionPerformed

