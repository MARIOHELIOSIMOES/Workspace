    private void preencherArrayList(){
        try{
            arrayList.clear();
            if(chAtivos.isSelected()){
                arrayList.addAll(ac.getArrayListAtivosByIdVeiculo(veiculo.getId()));
            }
            if(chInativos.isSelected()){
                arrayList.addAll(ac.getArrayListInativosByIdVeiculo(veiculo.getId()));
            }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpAvisos.preencherArrayList");
        }
    }
    private void preencherAvisoSelecionado(Aviso aviso){
        try{
            lblData.setText(aux.getDataString(aviso.getDatamilis()));
            lblId.setText(aviso.getId()+"");
            lblTitulo.setText(aviso.getTitulo());
            txaMensagem.setText(aviso.getMensagem());
            lblStatus.setText((aviso.isAtivo()?"Ativo": "Inativo"));
            if(aviso.isAtivo()){
                btnExcluirInativar.setText("Inativar");
            }else{
                 btnExcluirInativar.setText("Excluir");
            }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpAvisos.preencherAvisoSelecionado");
        }
    }
    private void preencherSelectedTable(){
        try{
            int i = jTable.getSelectedRow();
            if(i>=0 && arrayList.size()>0){
                preencherAvisoSelecionado(arrayList.get(arrayList.size()-i-1));
            }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpAvisos.preencherSelectedTable");
        }
    }
    private void preencherTabela(){
        DefaultTableModel model = new DefaultTableModelNaoEditavel();
        try{
            model.addColumn("N");
            model.addColumn("ID");
            model.addColumn("Data");
            model.addColumn("TÃ­tulo");
            model.addColumn("Mensagem");
            model.addColumn("Status");
            
            preencherArrayList();
            
            Object[] linha = new Object[6];
            for(int i = arrayList.size()-1; i >=0; i--){
                linha[0]= (i+1);
                linha[1]= arrayList.get(i).getId();
                linha[2]= aux.getDataString(arrayList.get(i).getDatamilis());
                linha[3]= arrayList.get(i).getTitulo();
                linha[4]= arrayList.get(i).getMensagem();
                linha[5]= (arrayList.get(i).isAtivo()? "Ativo":"Inativo");
                model.addRow(linha);
            }
            
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpAvisos.preencherTabela");
        }finally{
            jTable.setModel(model);
             jTable.getColumnModel().getColumn(0).setMaxWidth(35);
             jTable.getColumnModel().getColumn(1).setMaxWidth(35);
             jTable.getColumnModel().getColumn(2).setMaxWidth(70);
             jTable.getColumnModel().getColumn(5).setMaxWidth(70);
        
            
            lblNAtivos.setText(ac.getNAtivosByIdVeiculo(veiculo.getId())+"");
            if(arrayList.size()>0){
                jTable.addRowSelectionInterval(0, 0);
            }
        }
        
    }
    
    
    
    
