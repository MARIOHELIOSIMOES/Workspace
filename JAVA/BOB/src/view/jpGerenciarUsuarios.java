    private void preencherCbbNiveis(){
        try{
            cbbNivel.removeAllItems();
            cbbNivelNovo.removeAllItems();
            for(String tipo: Usuario.TIPOS){
                cbbNivel.addItem(tipo);
                cbbNivelNovo.addItem(tipo);
            }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpGerenciarUsuarios.preencherCBBNiveis");
        }
    }
    private void preencherArrayList(){
        try{
            arrayList = uCtrol.pesquisarTodosUsuarios(ckAdmin.isSelected(),ckAdmin.isSelected(),ckAdmin.isSelected());
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpGerenciarUsuarios.preencherArrayList");
        }
    }
    private void preencherSelectedTable(){
        try{
            int i = jTable.getSelectedRow();
            if(i>=0){
                usuarioSelecionado = arrayList.get(i);
                lblUsuario.setText(arrayList.get(i).getNome());
                cbbNivel.setSelectedIndex(arrayList.get(i).getTipo());
            }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpGerenciarUsuarios.preencherSelectedTable");
        }
    }
    private void preencherTabela(){
        DefaultTableModel model = new DefaultTableModelNaoEditavel();
        TableRowSorter tableSorter = new TableRowSorter(model);
        
        try{
            model.addColumn("Nº");
            model.addColumn("ID");
            model.addColumn("Nome");
            model.addColumn("Nível");
            Object[] linha;
            preencherArrayList();
            
            for(int i = 0; i< arrayList.size(); i++){
                 linha = new Object[4];
                 linha[0] = (i+1);
                 linha[1] = (arrayList.get(i).getId());
                 linha[2] = (arrayList.get(i).getNome());
                 linha[3] = (arrayList.get(i).getTipoDescricao());
                 model.addRow(linha);
            }
            tableSorter = new TableRowSorter(model);
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpGerenciarUsuarios.preencherTabela");
        }finally{
            jTable.setModel(model);
            jTable.getColumnModel().getColumn(0).setMaxWidth(50);
            jTable.getColumnModel().getColumn(1).setMaxWidth(50);
            jTable.setRowSorter(tableSorter);
            tableSorter.toggleSortOrder(0);
        }
    }
    

