    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
       try{
            int i = usuarioSelecionado.getId();
            if(i>0){
                Thread t = new Thread(new Runnable() {
                    @Override
                    public void run() {
                        usuarioSelecionado.setTipo(cbbNivel.getSelectedIndex());
                        uCtrol.alterarUsuario(usuarioSelecionado);
                        preencherTabela();
                    }});
                t.start();
            }
        }catch(Exception e){
            aux.RegistrarLog(e.getMessage(), "jpGerenciarUsuarios.btnAlterarActionPerformed");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

